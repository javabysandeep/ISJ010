package controlStatements.decisionMaking.switchDemos;

public class Demo4 {
    public static void main(String[] args) {
        switch (1) {
            case 1:
                System.out.println("one");
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                System.out.println("three");
                break;
            default:
                System.out.println("invalid number");
                break;
        }
    }
}
