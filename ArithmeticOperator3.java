public class ArithmeticOperator3 {

	public static void main(String[] args){

		System.out.println(10 / 20);//0
		System.out.println(10 % 20);//10
		System.out.println(1 / 20);//0
		System.out.println(100 / 2);//50
		System.out.println(101 / 2);//50
		System.out.println(101.0 / 2);//50.5
		System.out.println(1 / 10);//0
		System.out.println(1 % 10);//1			  		
		System.out.println('A'+1);//66
		//System.out.println(1 / 0);//AE
		//System.out.println(1 % 0);//AE
		System.out.println(1.0 / 0);//Infinity
		System.out.println(1.0 % 0);//NaN
				
	}

}